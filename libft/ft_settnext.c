#include "libft.h"

void	ft_settnext(t_stacks **s)
{
	int	j;

	j = 1;
	while ((++j < (ft_lstsize(*s) + 2)))
		ft_tlstlast(*s)->tnext = ft_tlstlast(*s)->next;
}
