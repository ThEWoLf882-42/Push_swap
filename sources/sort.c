#include "push_swap.h"

void	sort(t_stacks **a, t_stacks **b)
{
	int			i;

	i = 1;
	get_rank(*a);
	while ((*a))
	{
		if ((*a)->rank == i)
		{
			pb(a, b, 1);
			i++;
		}
		ra(a, 1);
	}
	while ((*b))
		pa(a, b, 1);
}
