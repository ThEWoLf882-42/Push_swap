#include "push_swap.h"

void	sort3(t_stacks **a)
{
	if (!its_ok(a))
	{
		while ((*a))
		{
			if (its_ok(a))
				break ;
			if ((*a)->rank == 3)
				ra(a, 1);
			else if ((*a)->rank == 2)
				sa(a, 1);
			else if (ft_lstlast((*a))->rank == 1)
				rra(a, 1);
		}
	}
}
