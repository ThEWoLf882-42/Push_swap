#include "push_swap.h"

void	sort5(t_stacks **a, t_stacks **b)
{
	get_rank(a);
	if (!its_ok(a))
	{
		while ((*a)->rank != 1)
			ra(a, 1);
		pb(a, b, 1);
		while ((*a)->rank != 2)
			ra(a, 1);
		pb(a, b, 1);
		sort3a(a);
		pa(a, b, 1);
		pa(a, b, 1);
	}
}
