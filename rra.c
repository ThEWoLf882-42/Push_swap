#include "push_swap.h"

void	rra(t_stacks **a)
{
	t_stacks	*m;

	if (ft_lstsize(&**a) > 1)
	{
		ft_setrr(a);
		m = ft_lstlast(&**a);
		m->tnext = (*a);
		(*a) = m;
		ft_setnext(a);
		ft_cleartnext(a);
	}
}
